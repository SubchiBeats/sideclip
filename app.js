$("#editorForm").addEventListener("submit", async event => {
  event.preventDefault();
  const button = $("#renderButton");
  const status = $("#renderStatus");
  if (!canvas.captureStream || !window.MediaRecorder) {
    status.textContent = "This browser does not support video rendering.";
    return;
  }
  button.disabled = true;
  status.textContent = "Rendering your 6-second clip...";
  const stream = canvas.captureStream(30);
  let audio;
  let audioContext;
  const selectedVoice = state.assets.find(asset => asset.id === state.voiceAsset);
  if (selectedVoice) {
    audio = new Audio(selectedVoice.url);
    audioContext = new AudioContext();
    const source = audioContext.createMediaElementSource(audio);
    const destination = audioContext.createMediaStreamDestination();
    source.connect(destination);
    source.connect(audioContext.destination);
    destination.stream.getAudioTracks().forEach(track => stream.addTrack(track));
  }
  const mimeType = MediaRecorder.isTypeSupported("video/webm;codecs=vp9") ? "video/webm;codecs=vp9" : "video/webm";
  const recorder = new MediaRecorder(stream, { mimeType, videoBitsPerSecond: 8000000 });
  const chunks = [];
  recorder.ondataavailable = e => { if (e.data.size) chunks.push(e.data); };
  recorder.onstop = () => {
    const blob = new Blob(chunks, {type: mimeType});
    const url = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.href = url;
    link.download = `sideclip-${Date.now()}.webm`;
    link.click();
    setTimeout(() => URL.revokeObjectURL(url), 3000);
    button.disabled = false;
    status.textContent = "Done. Your video was downloaded.";
    drawFrame(0);
  };
  recorder.start();
  if (audio) {
    audio.currentTime = 0;
    await audio.play();
  }
  const start = performance.now();
  await new Promise(resolve => {
    const render = now => {
      const elapsed = now - start;
      drawFrame(Math.min(1, elapsed / 6000));
      status.textContent = `Rendering... ${Math.min(100, Math.round(elapsed / 60))}%`;
      if (elapsed < 6000) requestAnimationFrame(render);
      else resolve();
    };
    requestAnimationFrame(render);
  });
  recorder.stop();
  if (audio) audio.pause();
  if (audioContext) audioContext.close();
});

async function loadSession() {
  try {
    const result = await api("/api/me");
    state.user = result.user;
    $("#engineLabel").textContent = result.ai === "ollama" ? "LOCAL AI ACTIVE" : "OFFLINE ENGINE";
    $("#engineDescription").textContent = `${result.ai === "ollama" ? "Private Ollama generation" : "Quality-gated local generation"} · v${result.version || "1.2.0"}`;
    updateAccountUi();
    if (state.user) {
      await Promise.all([loadProjects(), loadAssets()]);
      const lastProject = localStorage.getItem(LAST_PROJECT_KEY);
      const project = state.projects.find(item => item.id === lastProject) || state.projects[0];
      if (project) await loadProject(project.id, { quiet: true });
      else if (hasCampaignContent()) await saveProject({ silent: true });
    }
  } catch { toast("SideClip server is unavailable. Guest mode is active."); }
}

