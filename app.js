let voiceRecorder;
let voiceChunks = [];
$("#recordVoiceButton").addEventListener("click", async () => {
  if (voiceRecorder && voiceRecorder.state === "recording") {
    voiceRecorder.stop();
    $("#recordVoiceButton").textContent = "Record voiceover";
    return;
  }
  if (!state.user) { $("#authDialog").showModal(); return; }
  try {
    const stream = await navigator.mediaDevices.getUserMedia({ audio: true });
    voiceChunks = [];
    voiceRecorder = new MediaRecorder(stream, { mimeType: MediaRecorder.isTypeSupported("audio/webm") ? "audio/webm" : "" });
    voiceRecorder.ondataavailable = event => { if (event.data.size) voiceChunks.push(event.data); };
    voiceRecorder.onstop = () => {
      stream.getTracks().forEach(track => track.stop());
      const blob = new Blob(voiceChunks, { type: "audio/webm" });
      const reader = new FileReader();
      reader.onload = async () => {
        try {
          const result = await api("/api/uploads", { method: "POST", body: JSON.stringify({
            name: `Voiceover ${new Date().toLocaleString()}`, type: "audio/webm", data: reader.result
          }) });
          await loadAssets();
          state.voiceAsset = result.asset.id;
          renderAssets();
          toast("Voiceover recorded and selected.");
        } catch (error) { toast(error.message); }
      };
      reader.readAsDataURL(blob);
    };
    voiceRecorder.start();
    $("#recordVoiceButton").textContent = "Stop recording";
    toast("Recording voiceover...");
  } catch { toast("Microphone access is required to record a voiceover."); }
});
function downloadBlob(name, type, content) {
  const link = document.createElement("a");
  link.href = URL.createObjectURL(new Blob([content], { type }));
  link.download = name; link.click();
  setTimeout(() => URL.revokeObjectURL(link.href), 3000);
}
function srtTime(ms) {
  const hours = String(Math.floor(ms / 3600000)).padStart(2, "0");
  const minutes = String(Math.floor(ms / 60000) % 60).padStart(2, "0");
  const seconds = String(Math.floor(ms / 1000) % 60).padStart(2, "0");
  return `${hours}:${minutes}:${seconds},${String(Math.round(ms % 1000)).padStart(3, "0")}`;
}
$("#subtitleButton").addEventListener("click", () => {
  const parts = [state.current.hook, state.current.body, state.current.cta];
  const weights = parts.map(text => Math.max(1, String(text).trim().split(/\s+/).length));
  const total = weights.reduce((sum, count) => sum + count, 0);
  let cursor = 0;
  const content = parts.map((text, index) => {
    const start = cursor;
    cursor = index === parts.length - 1 ? 6000 : Math.min(5500, cursor + Math.round((weights[index] / total) * 6000));
    return `${index + 1}\n${srtTime(start)} --> ${srtTime(cursor)}\n${text}\n`;
  }).join("\n");
  downloadBlob("sideclip-captions.srt", "text/plain", content);
});
function platformCaption(platform) {
  const details = state.current.sourceCaption || state.current.caption || `${state.current.body}\n\n${state.current.cta}.`;
  const product = $("#product").value.trim() || "your business";
  const audience = $("#audience").value.trim() || "your audience";
  const variant = state.current.captionVariant || 0;
  const tags = `#${product.replace(/[^a-z0-9]/gi, "")} #ContentMarketing #SmallBusiness`;
  const nextStep = state.current.cta.replace(/[.!?]+$/, "").replace(/^./, letter => letter.toLowerCase());
  const openers = [
    `For ${audience}, this is worth knowing:`,
    `A practical look at ${product}:`,
    `Before you decide, consider this:`,
    `Here is the useful takeaway for ${audience}:`
  ];
  const closers = [
    `The next step: ${nextStep}.`,
    `Save this for later, then ${nextStep}.`,
    `Share this with someone who would benefit, then ${nextStep}.`,
    `${state.current.body}\n\nReady when you are: ${nextStep}.`
  ];
  const variants = {
    balanced: `${openers[variant % openers.length]}\n\n${details}\n\n${closers[variant % closers.length]}`,
    linkedin: `${openers[(variant + 1) % openers.length]}\n\n${details}\n\n${closers[(variant + 1) % closers.length]}\n\nQuestion: what part of this matters most to you?\n\n${tags}`,
    instagram: `Save this before your next post.\n\n${details}\n\n${closers[(variant + 2) % closers.length]}\n\n${tags} #SocialMediaTips`,
    tiktok: `Quick breakdown:\n\n${details}\n\n${closers[(variant + 3) % closers.length]}\n\n${tags} #LearnOnTikTok`
  };
  return variants[platform].slice(0, 1600);
}
