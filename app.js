function roundedRect(context, x, y, w, h, r) {
  context.beginPath();
  context.roundRect(x, y, w, h, r);
  context.fill();
}

function wrapText(context, text, maxWidth) {
  const words = text.split(" ").flatMap(word => {
    if (context.measureText(word).width <= maxWidth) return [word];
    const chunks = [];
    let chunk = "";
    for (const character of word) {
      if (context.measureText(chunk + character).width > maxWidth && chunk) {
        chunks.push(chunk);
        chunk = character;
      } else chunk += character;
    }
    if (chunk) chunks.push(chunk);
    return chunks;
  });
  const lines = [];
  let line = "";
  words.forEach(word => {
    const test = line ? `${line} ${word}` : word;
    if (context.measureText(test).width > maxWidth && line) {
      lines.push(line);
      line = word;
    } else line = test;
  });
  if (line) lines.push(line);
  return lines;
}

function fitTextBlock(context, text, maxWidth, startSize, minSize, weight, lineFactor, maxHeight) {
  for (let size = startSize; size >= minSize; size -= 1) {
    context.font = `${weight} ${size}px Manrope`;
    const lines = wrapText(context, text, maxWidth);
    const lineHeight = Math.round(size * lineFactor);
    if (lines.length * lineHeight <= maxHeight) return { lines, size, lineHeight, font: context.font };
  }
  context.font = `${weight} ${minSize}px Manrope`;
  const lineHeight = Math.round(minSize * lineFactor);
  const lines = wrapText(context, text, maxWidth).slice(0, Math.max(1, Math.floor(maxHeight / lineHeight)));
  return { lines, size: minSize, lineHeight, font: context.font };
}

function drawTextBlock(context, block, x, y, color) {
  context.fillStyle = color;
  context.font = block.font;
  context.textAlign = "left";
  context.textBaseline = "top";
  block.lines.forEach((line, index) => context.fillText(line, x, y + index * block.lineHeight));
}

function fitSingleLine(context, text, maxWidth, startSize, minSize, weight = 800) {
  let size = startSize;
  while (size > minSize) {
    context.font = `${weight} ${size}px Manrope`;
    if (context.measureText(text).width <= maxWidth) break;
    size -= 1;
  }
  return size;
}

function drawProcedural(context, style, progress, color) {
  const p = progress * Math.PI * 2;
  const glow = context.createRadialGradient(180, 245, 10, 180, 245, 300);
  glow.addColorStop(0, color + "b8");
  glow.addColorStop(1, "#11110f00");
  context.fillStyle = glow;
  context.fillRect(0, 0, 360, 430);
  context.save();
  context.strokeStyle = color + "70";
  context.fillStyle = color + "30";

  if (style === "checklist") {
    [120, 190, 260, 330].forEach((y, index) => {
      const offset = Math.sin(p + index) * 7;
      context.fillStyle = "#ffffff10";
      context.beginPath(); context.roundRect(45 + offset, y, 270, 48, 12); context.fill();
      context.fillStyle = color;
      context.beginPath(); context.arc(68 + offset, y + 24, 10, 0, Math.PI * 2); context.fill();
      context.strokeStyle = "#11110f"; context.lineWidth = 3;
      context.beginPath(); context.moveTo(63 + offset, y + 24); context.lineTo(67 + offset, y + 28); context.lineTo(74 + offset, y + 19); context.stroke();
      context.fillStyle = "#ffffff35"; context.fillRect(91 + offset, y + 17, 155 - index * 15, 5); context.fillRect(91 + offset, y + 29, 105 + index * 12, 4);
    });
  } else if (style === "spotlight") {
    [0, 1, 2].forEach(index => {
      const x = 90 + index * 90 + Math.sin(p + index * 2) * 25;
      const y = 210 + Math.cos(p + index) * 45;
      const radial = context.createRadialGradient(x, y, 5, x, y, 75);
      radial.addColorStop(0, color + "aa"); radial.addColorStop(1, color + "00");
      context.fillStyle = radial; context.fillRect(x - 80, y - 80, 160, 160);
    });
  } else if (style === "cards") {
    [-1, 0, 1].forEach((index, position) => {
      context.save();
      context.translate(180 + index * 82, 225 + Math.sin(p + position) * 12);
      context.rotate(index * .12 + Math.sin(p) * .025);
      context.fillStyle = position === 1 ? color + "aa" : "#ffffff16";
      context.beginPath(); context.roundRect(-52, -78, 104, 156, 15); context.fill();
      context.strokeStyle = "#ffffff30"; context.stroke();
      context.restore();
    });
  } else if (style === "grid") {
    context.lineWidth = 1;
    for (let x = 20; x < 360; x += 42) { context.beginPath(); context.moveTo(x, 75); context.lineTo(x, 410); context.stroke(); }
    for (let y = 75; y < 420; y += 42) { context.beginPath(); context.moveTo(15, y); context.lineTo(345, y); context.stroke(); }
    for (let index = 0; index < 7; index++) {
      context.fillStyle = index % 2 ? color : "#ffffff";
      context.beginPath(); context.arc(54 + index * 42, 117 + ((index * 3) % 6) * 42, 4 + Math.sin(p + index) * 2, 0, Math.PI * 2); context.fill();
    }
  } else if (style === "waves") {
    context.lineWidth = 3;
    for (let row = 0; row < 7; row++) {
      context.beginPath();
      for (let x = 0; x <= 360; x += 8) {
        const y = 130 + row * 38 + Math.sin(x / 35 + p + row * .7) * 18;
        x ? context.lineTo(x, y) : context.moveTo(x, y);
      }
      context.stroke();
    }
  } else {
    context.translate(180, 240);
    context.rotate(progress * .5);
    context.lineWidth = 35;
    context.beginPath();
    context.arc(0, 0, 145 + Math.sin(progress * 4) * 8, 0, Math.PI * 1.65);
    context.stroke();
  }
  context.restore();
}

function drawFrame(progress = 0) {
  const { format, hook, body, cta, color, visual = "orbit" } = state.current;
  const scale = canvas.width / 360;
  const quality = copyQuality(state.current);
  const bodyFits = !quality.issues.some(issue => issue.startsWith("Supporting line must be"));
  const renderBody = bodyFits ? body : "Supporting line must be 45-145 characters before rendering.";
  ctx.setTransform(1, 0, 0, 1, 0, 0);
  ctx.fillStyle = "#11110f";
  ctx.fillRect(0, 0, canvas.width, canvas.height);
  ctx.setTransform(scale, 0, 0, scale, 0, 0);
  drawProcedural(ctx, visual, progress, color);
  if (state.assetElement) {
    const media = state.assetElement;
    const sourceWidth = media.videoWidth || media.naturalWidth || 360;
    const sourceHeight = media.videoHeight || media.naturalHeight || 640;
    const scale = Math.max(360 / sourceWidth, 640 / sourceHeight);
    const width = sourceWidth * scale;
    const height = sourceHeight * scale;
    ctx.globalAlpha = .48;
    ctx.drawImage(media, (360 - width) / 2, (640 - height) / 2, width, height);
    ctx.globalAlpha = 1;
    ctx.fillStyle = "#11110f88";
    ctx.fillRect(0, 0, 360, 640);
  }
  ctx.fillStyle = color;
  roundedRect(ctx, 24, 26, 82, 23, 12);
  ctx.fillStyle = "#11110f";
  ctx.font = "700 9px Manrope";
  ctx.textAlign = "left";
  ctx.textBaseline = "alphabetic";
  ctx.fillText(format.toUpperCase(), 40, 41);

  const hookBlock = fitTextBlock(ctx, hook, 310, 34, 13, 800, 1.08, 244);
  const hookHeight = hookBlock.lines.length * hookBlock.lineHeight;
  const hookY = Math.max(170, 414 - hookHeight);
  ctx.save();
  ctx.beginPath();
  ctx.rect(18, 160, 324, 260);
  ctx.clip();
  drawTextBlock(ctx, hookBlock, 25, hookY, "#ffffff");
  ctx.restore();

  ctx.globalAlpha = progress === 0 ? 1 : Math.min(1, .45 + progress * 1.8);
  ctx.fillStyle = "#11110fcc";
  roundedRect(ctx, 18, 430, 324, 116, 16);
  ctx.strokeStyle = color + "77";
  ctx.lineWidth = 1;
  ctx.beginPath();
  ctx.roundRect(18.5, 430.5, 323, 115, 15.5);
  ctx.stroke();
  ctx.fillStyle = color;
  ctx.font = "700 8px DM Mono";
  ctx.textAlign = "left";
  ctx.textBaseline = "top";
  ctx.fillText("WHY IT MATTERS", 32, 446);
  const bodyBlock = fitTextBlock(ctx, renderBody, 296, 14, 9, 600, 1.28, 70);
  ctx.save();
  ctx.beginPath();
  ctx.rect(30, 464, 300, 72);
  ctx.clip();
  drawTextBlock(ctx, bodyBlock, 32, 466, bodyFits ? "#f2efe5" : "#ff927f");
  ctx.restore();
  ctx.globalAlpha = 1;

  ctx.shadowColor = "#00000066";
  ctx.shadowBlur = 14 * scale;
  ctx.shadowOffsetY = 5 * scale;
  const ctaGradient = ctx.createLinearGradient(24, 574, 336, 620);
  ctaGradient.addColorStop(0, color);
  ctaGradient.addColorStop(1, "#ffffff");
  ctx.fillStyle = ctaGradient;
  roundedRect(ctx, 24, 574, 312, 46, 23);
  ctx.shadowColor = "transparent";
  ctx.fillStyle = "#11110f";
  const ctaLabel = `${cta}  →`;
  const ctaSize = fitSingleLine(ctx, ctaLabel, 270, 13, 10);
  ctx.font = `800 ${ctaSize}px Manrope`;
  ctx.textAlign = "center";
  ctx.textBaseline = "middle";
  ctx.fillText(ctaLabel, 180, 597);
  ctx.fillStyle = "#ffffff88";
  ctx.font = "500 8px DM Mono";
  ctx.textAlign = "left";
  ctx.textBaseline = "alphabetic";
  ctx.fillText("MADE WITH SIDECLIP", 24, 563);
}

